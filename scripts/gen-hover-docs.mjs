#!/usr/bin/env node
// gen-hover-docs.mjs — regenerate the HOVER_DOCS table in lsp-hover.mjs from the
// CircleCI config schema for the version pinned in the launcher.
//
// Usage: node scripts/gen-hover-docs.mjs [--check]
//   (no args) rewrites the <generated:hover-docs> block in lsp-hover.mjs in place.
//   --check   exits 1 if the committed block differs from what would be
//             regenerated (used by CI to catch drift); status goes to stderr.
//
// No runtime deps; this runs only at maintenance/CI time, never at LSP startup.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const REPO = "CircleCI-Public/circleci-yaml-language-server";
const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const launcher = join(root, "plugins/circleci-yaml-lsp/bin/circleci-yaml-lsp");
const hoverFile = join(root, "plugins/circleci-yaml-lsp/bin/lsp-hover.mjs");

const BEGIN = "// <generated:hover-docs>";
const END = "// </generated:hover-docs>";

function pinnedVersion() {
  const m = /^VERSION="([^"]+)"/m.exec(readFileSync(launcher, "utf8"));
  if (!m) throw new Error("could not find VERSION= in launcher");
  return m[1];
}

async function fetchSchema(version) {
  const url = `https://github.com/${REPO}/releases/download/${version}/schema.json`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`schema download failed (${res.status}): ${url}`);
  return JSON.parse(await res.text());
}

// Resolve a local "#/..." $ref against the root schema.
function makeResolver(schema) {
  return function resolveRef(ref) {
    if (typeof ref !== "string" || !ref.startsWith("#/")) return null;
    let cur = schema;
    for (const p of ref.slice(2).split("/")) { cur = cur?.[p]; if (cur == null) return null; }
    return cur;
  };
}

// First markdownDescription for a property def: direct, else one level into
// $ref / oneOf / anyOf / allOf / items / then / else (depth-bounded). Falls back to a
// plain `description` only as a last resort, so it adds coverage if upstream ever stops
// setting markdownDescription without changing any resolution that already finds one.
function descOf(def, resolveRef, depth = 0) {
  if (!def || typeof def !== "object" || depth > 3) return null;
  if (typeof def.markdownDescription === "string" && def.markdownDescription.trim()) {
    return def.markdownDescription.trim();
  }
  if (def.$ref) { const d = descOf(resolveRef(def.$ref), resolveRef, depth + 1); if (d) return d; }
  for (const kw of ["oneOf", "anyOf", "allOf"]) {
    if (Array.isArray(def[kw])) for (const sub of def[kw]) { const d = descOf(sub, resolveRef, depth + 1); if (d) return d; }
  }
  for (const kw of ["items", "then", "else"]) {
    if (def[kw]) { const d = descOf(def[kw], resolveRef, depth + 1); if (d) return d; }
  }
  if (typeof def.description === "string" && def.description.trim()) {
    return def.description.trim();
  }
  return null;
}

function extract(schema) {
  const resolveRef = makeResolver(schema);
  const map = new Map();
  (function walk(obj) {
    if (!obj || typeof obj !== "object") return;
    for (const [k, v] of Object.entries(obj)) {
      if (k === "properties" && v && typeof v === "object") {
        for (const [prop, def] of Object.entries(v)) {
          if (map.has(prop)) continue;
          const d = descOf(def, resolveRef);
          if (d) map.set(prop, d);
        }
      }
      if (k !== "markdownDescription") walk(v);
    }
  })(schema);
  return map;
}

function renderBlock(map, version) {
  const entries = [...map.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  let out = `${BEGIN} — regenerated by scripts/gen-hover-docs.mjs from schema.json @ ${version}; do not edit by hand.\n`;
  out += "const HOVER_DOCS = {\n";
  for (const [k, v] of entries) out += `  ${JSON.stringify(k)}: ${JSON.stringify(v)},\n`;
  out += "};\n";
  out += END;
  return out;
}

function spliceBlock(source, newBlock) {
  const start = source.indexOf(BEGIN);
  const end = source.indexOf(END);
  if (start === -1 || end === -1) throw new Error(`markers ${BEGIN} / ${END} not found in lsp-hover.mjs`);
  return source.slice(0, start) + newBlock + source.slice(end + END.length);
}

const version = pinnedVersion();
const schema = await fetchSchema(version);
const block = renderBlock(extract(schema), version);
const current = readFileSync(hoverFile, "utf8");
const updated = spliceBlock(current, block);

if (process.argv.includes("--check")) {
  if (updated !== current) {
    process.stderr.write("HOVER_DOCS is stale — run `npm run gen:hover-docs` and commit.\n");
    process.exit(1);
  }
  process.stderr.write("HOVER_DOCS is up to date.\n");
  process.exit(0);
}

writeFileSync(hoverFile, updated);
process.stderr.write(`Wrote ${hoverFile} from schema.json @ ${version}.\n`);
